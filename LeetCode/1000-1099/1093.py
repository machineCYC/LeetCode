from typing import List

class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        max_v = 0
        min_v = 10**9
        average = 0
        median = 0
        mode = 0

        max_nbr = 0
        total = 0
        nbr = 0
        for i, c in enumerate(count):
            if c:
                min_v = min(min_v, i)
                max_v = max(max_v, i)
                total += i * c
                nbr += c
                average = total / nbr
                if c > max_nbr:
                    max_nbr = c
                    mode = i

        left_median = 0
        nnbr = 0
        for i, c in enumerate(count):
            if c:
                nnbr += c
                if nnbr >= (nbr / 2):
                    left_median = i
                    break

        right_median = 0
        nnbr = 0
        for i, c in reversed(list(enumerate(count))):
            if c:
                nnbr += c
                if nnbr >= (nbr / 2):
                    right_median = i
                    break

        median = (left_median + right_median) / 2
        return [min_v, max_v, average, median, mode]

